#!/usr/bin/env node
// Generates a worst-covered-first markdown report from coverage-summary.json
// and writes it to coverage/coverage-gaps-report.md. Run after `pnpm test:coverage`.
//
// This is the source of the pinned "Coverage Gaps (auto-updated)" GitHub issue
// (see .github/workflows/ci.yml's coverage job, push-to-main only) — a standing,
// agent-actionable backlog: whoever has downtime can open that issue and grab
// the file at the top of the list. Sorted ascending by line % on purpose, so
// "top of the list" always means "worst gap first," not alphabetical.

import { readFileSync, writeFileSync, existsSync } from 'node:fs'

const summaryPath = process.env.COVERAGE_SUMMARY_JSON || 'coverage/coverage-summary.json'
const reportPath = process.env.COVERAGE_GAPS_REPORT || 'coverage/coverage-gaps-report.md'

function pct(n) {
  return n === undefined ? '—' : n.toFixed(2) + '%'
}

function main() {
  if (!existsSync(summaryPath)) {
    console.error(`report-coverage-gaps: ${summaryPath} not found — run pnpm test:coverage first`)
    process.exit(1)
  }
  const data = JSON.parse(readFileSync(summaryPath, 'utf8'))
  const { total, ...files } = data

  const rows = Object.entries(files)
    .map(([path, f]) => {
      const marker = '/holmgard-lore-mcp/'
      const idx = path.lastIndexOf(marker)
      const rel = idx === -1 ? path : path.slice(idx + marker.length)
      return {
        rel,
        lines: f.lines.pct,
        statements: f.statements.pct,
        functions: f.functions.pct,
        branches: f.branches.pct,
      }
    })
    .sort((a, b) => a.lines - b.lines)

  let md = '## Coverage Gaps (auto-updated)\n\n'
  md +=
    'This issue is a standing backlog of the worst-covered files in `src/`, auto-updated by the `coverage`\n' +
    'CI job on every push to `main`. Intended as pick-up-able work for downtime — an idle agent (or a human)\n' +
    'can grab the file at the top of the list and add tests.\n\n'
  md +=
    '**Do not edit this body by hand** — it gets overwritten on the next push to `main`. Use comments for discussion instead.\n\n'
  md += `_Last updated: ${new Date().toISOString()}_\n\n`

  md += '### Project totals\n\n'
  md += '| Metric | Covered / Total | % |\n'
  md += '|---|---|---|\n'
  md += `| Lines | ${total.lines.covered} / ${total.lines.total} | ${pct(total.lines.pct)} |\n`
  md += `| Statements | ${total.statements.covered} / ${total.statements.total} | ${pct(total.statements.pct)} |\n`
  md += `| Functions | ${total.functions.covered} / ${total.functions.total} | ${pct(total.functions.pct)} |\n`
  md += `| Branches | ${total.branches.covered} / ${total.branches.total} | ${pct(total.branches.pct)} |\n\n`

  md += '### Worst-covered files (lines %, ascending — top of the list is the biggest gap)\n\n'
  md += '| File | Lines | Statements | Functions | Branches |\n'
  md += '|---|---|---|---|---|\n'
  for (const r of rows) {
    md += `| \`${r.rel}\` | ${pct(r.lines)} | ${pct(r.statements)} | ${pct(r.functions)} | ${pct(r.branches)} |\n`
  }
  md +=
    '\n_Generated by `scripts/report-coverage-gaps.mjs` from `coverage/coverage-summary.json`._\n'

  writeFileSync(reportPath, md)
  console.log(`report-coverage-gaps: wrote ${rows.length} file(s) to ${reportPath}`)
}

main()
